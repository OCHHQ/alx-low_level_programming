#include <stdio.h>
/**
 * add - add two integers
 * @a: 1st unt
 * @b: second int
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - sub two integers
 * @a: 1st int
 * @b: second int
 * Return: subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * div - division two integers
 * @a: 1st unt
 * @b: second int
 * Return: division 
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - mod two integers
 * @a: 1st unt
 * @b: second int
 * Return: final ans
 */
int mod(int a, int b)
{
	return (a % b);
}

/**
 * mul - multiply two integers
 * @a: 1st unt
 * @b: second int
 * Return: multiplication of numbers
 */
int mul(int a, int b)
{
	return (a * b);
}
